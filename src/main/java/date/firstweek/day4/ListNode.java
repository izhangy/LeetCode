package date.firstweek.day4;

/**
 * @author When all else is lost the future still remains.
 * @date 2021/6/3 - 9:13
 **/
public class ListNode {
    int val;

    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
